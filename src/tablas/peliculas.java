package tablas;

/**
 *
 * @author Marcelo Cazon & Pablo Carrizo
 */
public class peliculas {

    private int id;
    private String nombre;
    private String genero;
    private String duracion;
    private String director;
    private String actores;
    private String fechaestreno;
    private int nro_copias;
    private String sinopsis;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the genero
     */
    public String getGenero() {
        return genero;
    }

    /**
     * @param genero the genero to set
     */
    public void setGenero(String genero) {
        this.genero = genero;
    }

    /**
     * @return the duracion
     */
    public String getDuracion() {
        return duracion;
    }

    /**
     * @param duracion the duracion to set
     */
    public void setDuracion(String duracion) {
        this.duracion = duracion;
    }

    /**
     * @return the director
     */
    public String getDirector() {
        return director;
    }

    /**
     * @param director the director to set
     */
    public void setDirector(String director) {
        this.director = director;
    }

    /**
     * @return the actores
     */
    public String getActores() {
        return actores;
    }

    /**
     * @param actores the actores to set
     */
    public void setActores(String actores) {
        this.actores = actores;
    }

    /**
     * @return the fechaestreno
     */
    public String getFechaestreno() {
        return fechaestreno;
    }

    /**
     * @param fechaestreno the fechaestreno to set
     */
    public void setFechaestreno(String fechaestreno) {
        this.fechaestreno = fechaestreno;
    }

    /**
     * @return the nro_copias
     */
    public int getNro_copias() {
        return nro_copias;
    }

    /**
     * @param nro_copias the nro_copias to set
     */
    public void setNro_copias(int nro_copias) {
        this.nro_copias = nro_copias;
    }

    /**
     * @return the sinopsis
     */
    public String getSinopsis() {
        return sinopsis;
    }

    /**
     * @param sinopsis the sinopsis to set
     */
    public void setSinopsis(String sinopsis) {
        this.sinopsis = sinopsis;
    }

    
}