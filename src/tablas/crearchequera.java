public class crearchequera {

    private int idcliente;
    private String nombre_cliente;
    private String chequera;

    /**
     * @return the idcliente
     */
    public int getIdcliente() {
        return idcliente;
    }

    /**
     * @param idcliente the idcliente to set
     */
    public void setIdcliente(int idcliente) {
        this.idcliente = idcliente;
    }

    /**
     * @return the nombre_cliente
     */
    public String getNombre_cliente() {
        return nombre_cliente;
    }

    /**
     * @param nombre_cliente the nombre_cliente to set
     */
    public void setNombre_cliente(String nombre_cliente) {
        this.nombre_cliente = nombre_cliente;
    }

    /**
     * @return the chequera
     */
    public String getChequera() {
        return chequera;
    }

    /**
     * @param chequera the chequera to set
     */
    public void setChequera(String chequera) {
        this.chequera = chequera;
    }

}