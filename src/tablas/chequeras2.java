public class chequeras2 {

    private int dni_cliente;
    private String nombre_cliente;
    private int cant_peliculas;

    /**
     * @return the dni_cliente
     */
    public int getDni_cliente() {
        return dni_cliente;
    }

    /**
     * @param dni_cliente the dni_cliente to set
     */
    public void setDni_cliente(int dni_cliente) {
        this.dni_cliente = dni_cliente;
    }

    /**
     * @return the nombre_cliente
     */
    public String getNombre_cliente() {
        return nombre_cliente;
    }

    /**
     * @param nombre_cliente the nombre_cliente to set
     */
    public void setNombre_cliente(String nombre_cliente) {
        this.nombre_cliente = nombre_cliente;
    }

    /**
     * @return the cant_peliculas
     */
    public int getCant_peliculas() {
        return cant_peliculas;
    }

    /**
     * @param cant_peliculas the cant_peliculas to set
     */
    public void setCant_peliculas(int cant_peliculas) {
        this.cant_peliculas = cant_peliculas;
    }

}